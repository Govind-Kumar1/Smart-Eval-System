const mongoose = require('mongoose');

const submissionSchema = new mongoose.Schema({
  student: { type: mongoose.Schema.Types.ObjectId, ref: 'User', required: true },
  problem: { type: mongoose.Schema.Types.ObjectId, ref: 'Problem', required: true },
  code: { type: String, required: true },
  language: { type: String, default: 'cpp' },
  status: { 
    type: String, 
    enum: ['Passed', 'Failed', 'Compilation Error', 'Runtime Error'] 
  },
  
  errorLog: { type: String }, // Raw Error (jo compiler ne diya)
  
  // Kitne test cases the aur kitne paas hue
  passedTestCases: { type: Number, default: 0 },
  totalTestCases: { type: Number, default: 0 }, // --- NAYA FIELD (Score dikhane ke liye e.g., 3/5) ---

  // 1. AI Recommendation (Automatic generated by System)
  aiRecommendation: { type: String, default: "" },

  // 2. Teacher Feedback (Manual by Professor)
  teacherFeedback: { type: String, default: "" } 

}, { timestamps: true });

module.exports = mongoose.model('Submission', submissionSchema);